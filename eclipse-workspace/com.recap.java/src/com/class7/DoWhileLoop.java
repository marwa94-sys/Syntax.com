package com.class7;



public class DoWhileLoop {
	public static void main(String[] args) {
		
		System.out.println("-----------USING WHILE LOOP----------------");
		
	int num=1;
	
	
	while (num<=5) {
		System.out.println("Hello");
		num++;
	}
		
		System.out.println("--------------USING DO WHILE LOOP-------------------");
	
	do {
		System.out.println("Hello");
		num++;
	} while (num<10);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
